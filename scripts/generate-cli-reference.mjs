#!/usr/bin/env node
// Generates the static CLI flag reference in command-line-options.md from the
// Prysm help endpoint. Pass --check to exit 1 if the committed file is stale.

import { readFile, writeFile } from 'node:fs/promises';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';

const ENDPOINT = 'https://cli.prylabs.network';
const COMPONENTS = ['beacon-chain', 'validator', 'prysmctl', 'client-stats'];

const __dirname = dirname(fileURLToPath(import.meta.url));
const DOC_PATH = resolve(__dirname, '../docs/configure-prysm/command-line-options.md');

async function fetchHelp(component) {
  const res = await fetch(`${ENDPOINT}/${component}`);
  if (!res.ok) throw new Error(`${component}: HTTP ${res.status} from ${ENDPOINT}`);
  const text = (await res.text()).trim();
  if (!text) throw new Error(`${component}: empty response from ${ENDPOINT}`);
  return text;
}

function block(component, help) {
  const start = `{/* cli-flags:${component} START — auto-generated by scripts/generate-cli-reference.mjs, do not edit by hand */}`;
  const end = `{/* cli-flags:${component} END */}`;
  return `${start}\n\n\`\`\`\n${help}\n\`\`\`\n\n${end}`;
}

function splice(doc, component, help) {
  const re = new RegExp(
    `\\{/\\* cli-flags:${component} START[^]*?cli-flags:${component} END \\*/\\}`,
  );
  if (!re.test(doc)) {
    throw new Error(`marker for "${component}" not found in ${DOC_PATH}`);
  }
  return doc.replace(re, block(component, help));
}

async function main() {
  const check = process.argv.includes('--check');
  const current = await readFile(DOC_PATH, 'utf8');

  let next = current;
  for (const component of COMPONENTS) {
    next = splice(next, component, await fetchHelp(component));
  }

  if (check) {
    if (next !== current) {
      console.error('CLI reference is stale. Run: npm run generate-cli-reference');
      process.exit(1);
    }
    console.log('CLI reference is up to date.');
    return;
  }

  if (next === current) {
    console.log('CLI reference unchanged.');
    return;
  }
  await writeFile(DOC_PATH, next);
  console.log('Wrote', DOC_PATH);
}

main().catch((err) => {
  console.error(err.message);
  process.exit(1);
});
